export const FAKE_EVENTS: CalendarEvent[] = RAW_EVENTS;

export function getEvent(id: string): CalendarEvent | undefined {
  return FAKE_EVENTS.find((e) => e.id === id);
}

export function getEventsByOrganizer(organizerId: string): CalendarEvent[] {
  return FAKE_EVENTS.filter((e) => e.organizerId === organizerId);
}

export function getEventsByVenue(venueId: string): CalendarEvent[] {
  return FAKE_EVENTS.filter((e) => e.venueId === venueId);
}

export function getEventsBySeries(seriesId: string): CalendarEvent[] {
  return FAKE_EVENTS.filter((e) => e.seriesId === seriesId);
}

export function getUpcomingEvents(): CalendarEvent[] {
  const now = new Date().toISOString();
  return FAKE_EVENTS
    .filter((e) => e.endTime > now && e.status !== "cancelled" && e.status !== "completed")
    .sort((a, b) => a.startTime.localeCompare(b.startTime));
}

export function getHappeningNow(): CalendarEvent[] {
  const now = new Date().toISOString();
  return FAKE_EVENTS.filter(
    (e) => e.startTime <= now && e.endTime > now && e.status === "in_progress",
  );
}

export function getFeaturedEvents(): CalendarEvent[] {
  return FAKE_EVENTS.filter((e) => e.featured && e.status === "published");
}
