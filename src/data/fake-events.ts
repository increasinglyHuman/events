/* ─── helpers ───────────────────────────────────────────────── */

const NOW = new Date();

/** Offset hours from now → ISO string */
function h(offset: number): string {
  return new Date(NOW.getTime() + offset * 3600_000).toISOString();
}

/** Offset days from today at a given hour (0-23) → ISO string */
function d(days: number, hour = 12, min = 0): string {
  const dt = new Date(NOW);
  dt.setDate(dt.getDate() + days);
  dt.setHours(hour, min, 0, 0);
  return dt.toISOString();
}

function tickets(tiers: [string, number, string, number, number][]): TicketTier[] {
  return tiers.map(([name, price, currency, available, total], i) => ({
    id: `tkt-${i}`,
    name,
    price,
    currency,
    available,
    total,
  }));
}

function recur(
  frequency: RecurrenceRule["frequency"],
  seriesId: string,
  interval = 1,
  daysOfWeek?: number[],
): RecurrenceRule {
  return { frequency, interval, seriesId, daysOfWeek };
}

